/*
Das If-Else Statement lässt den Code eine Entscheidung treffen.
Falls die Bedingung wahr ist, wird der erste Block ausgeführt, sonst der zweite Block:

if(Bedingung){

    Block, falls die Bedingung zutrifft

}else{

    Block falls die Bedingung nicht zutrtifft.

}


Bedingungen sind oft Vergleiche zwischen zwei Werten, die mit folgenden Operatoren durchgeführt werden können:

var1 == var2 //Ist var1 das gleiche wie var2?
var1 > var2  //Ist var1 grösser als var2?
var1 < var2  //Ist var1 kleiner als var2?
var1 >= var2 //Ist var1 grösser oder gleich gross wie var2?
var1 <= var2 //Ist var2 kleiner oder gleich gross wie var2?

Natürlich müssen nicht immer zwei Variabeln miteinander verglichen werden,
es könnte auch eine Variable mit einem festen Wert verglichen werden:

Beispiel:

let var1 = 32;
if (var1 < 34) {
    console.log('var1 ist kleiner als 34');
} else {
    console.log('var1 ist grösser als oder gleich gross wie 34');
}
  

Bedingungen und das If-Else Statement sind sehr wichtige Basics des Programmierens.
Stelle sicher das du die Erklärung verstanden hast.



Aufgabe:
Erstelle eine Variabel mit einer Zahl als Inhalt (z.B. let zahl = 45;), dann erstelle eine zweite Variabel mit dem gleichen Inhalt. 
Danach erstellst du eine dritte Variabel mit einem Text als inhalt.
Vergleiche nun die erste Variabel mit der zweiten Variabel und die erste Variabel mit der dritten Variabel.
Benutze dazu das If-Else Statement und zur ausgabe console.log();
*/
